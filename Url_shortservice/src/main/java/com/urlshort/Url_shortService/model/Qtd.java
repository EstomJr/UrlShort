package com.urlshort.Url_shortService.model;

public class Qtd {

    public int getQtd() {
        return qtd;
    }

    public void setQtd(int qtd) {
        this.qtd = qtd;
    }

    private int qtd;



    @Override
    public String toString() {
        return "Qtd{" +
                "qtd='" + qtd + '\'' +
                '}';
    }
}
